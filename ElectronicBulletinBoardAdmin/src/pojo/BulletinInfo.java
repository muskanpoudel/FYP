package pojo;

/**
 *
 * @author Muskan
 */
public class BulletinInfo {

    String bulletinId;
    String title;
    String noticeboardName;
    String noticeboardLocation;
    String date;
    String type;
    String contentFeederName;
    String contentFeederId;

    public String getBulletinId() {
        return bulletinId;
    }

    public void setBulletinId(String bulletinId) {
        this.bulletinId = bulletinId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getNoticeboardName() {
        return noticeboardName;
    }

    public void setNoticeboardName(String noticeboardName) {
        this.noticeboardName = noticeboardName;
    }

    public String getNoticeboardLocation() {
        return noticeboardLocation;
    }

    public void setNoticeboardLocation(String noticeboardLocation) {
        this.noticeboardLocation = noticeboardLocation;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getContentFeederName() {
        return contentFeederName;
    }

    public void setContentFeederName(String contentFeederName) {
        this.contentFeederName = contentFeederName;
    }

    public String getContentFeederId() {
        return contentFeederId;
    }

    public void setContentFeederId(String contentFeederId) {
        this.contentFeederId = contentFeederId;
    }

}
